package classqspider.assignment;

public class NestedLoopAssignment {
    public static void main(String[] args) {
        int a = 0;
        for (int i = 1; i <= 3; i++) {
            for (int j = 1; j <= 3; j++) {
                a++;
                System.out.print(a);
            }
            System.out.println();


        }
    }
}
