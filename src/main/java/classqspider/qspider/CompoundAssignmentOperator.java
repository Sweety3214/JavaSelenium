package classqspider.qspider;

public class CompoundAssignmentOperator {
    public static void main(String[] args) {
        int wallet = 2000;
        wallet += 1;  // wallet =wallet +1
        System.out.println(wallet);

        int my = 100;
        my -= 1;  //my=my-1
        System.out.println(my);

        int you = 2;
        you *= 4;
        System.out.println(you);
    }
}
