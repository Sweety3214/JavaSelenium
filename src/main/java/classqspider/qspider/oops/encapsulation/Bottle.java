package classqspider.qspider.oops.encapsulation;

public class Bottle {
    private String bColor;
    private int bRate;
    private int numOfBottle;

    public String getbColor() {
        return bColor;
    }

    public void setbColor(String bColor) {
        this.bColor = bColor;
    }

    public int getbRate() {
        return bRate;
    }

    public void setbRate(int bRate) {
        this.bRate = bRate;
    }

    public int getNumOfBottle() {
        return numOfBottle;
    }

    public void setNumOfBottle(int numOfBottle) {
        this.numOfBottle = numOfBottle;
    }
}
