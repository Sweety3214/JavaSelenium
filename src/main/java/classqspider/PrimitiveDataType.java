package classqspider;

public class PrimitiveDataType {
    public static void main(String[] args) {
        int num = 10 + 20;
        float ft = 23.3f;


        char al = 'a';
        String word = "69" + 8;
        boolean tf = true;

        System.out.println(num);
        System.out.println(ft);
        System.out.println(al);
        System.out.println(word);
        System.out.println(tf);


    }
}
